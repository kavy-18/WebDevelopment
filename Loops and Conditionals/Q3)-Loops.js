// loops are used to repetitively execute a piece of code
// for loop:
for(let i=0;i<5;i++){
    console.log("I am a loop");
}
// While loop:
let i=0;
while(i<5){
    console.log("I am a loop");
    i++;
}
// do-while loop:
i=0;
do{
    console.log("I am a loop");
    i++;
}while(i<5);